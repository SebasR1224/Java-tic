    public void toList() {
        List<Cliente> list = dao.toList();
        model = (DefaultTableModel) table.getModel();
        Object[] ob = new Object[6];
        for (int i = 0; i < list.size(); i++) {
            ob[0] = list.get(i).getUsername();
            ob[1] = list.get(i).getPassword();
            ob[2] = list.get(i).getNombre();
            ob[3] = list.get(i).getApellido();
            ob[4] = list.get(i).getEmail();
            ob[5] = list.get(i).getTelefono();
            model.addRow(ob);
        }
        table.setModel(model);
    }

