    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
        fila = table.getSelectedRow();
        String username = table.getValueAt(fila, 0).toString();
        String password = table.getValueAt(fila, 1).toString();
        String nombre = table.getValueAt(fila, 2).toString();
        String apellido = table.getValueAt(fila, 3).toString();
        String email = table.getValueAt(fila, 4).toString();
        String telefono = table.getValueAt(fila, 5).toString();

        this.username.setText(username);
        this.username.setEditable(false);
        this.password.setText(password);
        this.nombre.setText(nombre);
        this.apellido.setText(apellido);
        this.email.setText(email);
        this.telefono.setText(telefono);
    }//GEN-LAST:event_tableMouseClicked

